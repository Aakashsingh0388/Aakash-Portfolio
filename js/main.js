/*=============== VIDEO PLAY/PAUSE ON HOVER/TOUCH AND SCROLL ===============*/
const videos = document.querySelectorAll(".work__video");

function pauseAllVideos() {
  videos.forEach((video) => {
    video.pause();
  });
}

videos.forEach((video) => {
  // Play on hover (desktop)
  video.addEventListener("mouseover", () => {
    pauseAllVideos();
    video.play();
  });

  // Play on touch (mobile)
  video.addEventListener("touchstart", (e) => {
    e.preventDefault(); // Prevent scroll or other default touch behaviors
    pauseAllVideos();
    video.play();
  });
});

// Pause all videos on scroll
window.addEventListener("scroll", () => {
  pauseAllVideos();
});

